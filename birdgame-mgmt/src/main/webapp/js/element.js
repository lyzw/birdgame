/**
* 得到新数据后重新画   K线图
* 
**/
function newDataKLineChart(serverdata,kline,dataKey){
	var xAxisData = [];
	var map = {};
	map = serverdata[dataKey];
	var series_data=[];
	for(var rl in map){
		var kys = map[rl];
		for(var ky in kys){
			xAxisData.push(ky);
			series_data.push(kys[ky]);
		}
	}
	var chart_option = kline.getOption();
	kline.restore();
    kline.clear();
	chart_option.title.subtext="更新时间："+serverdata['update_time'];;
	chart_option.series[0].data = series_data;
	chart_option.xAxis[0].data = xAxisData;
	kline.setOption(chart_option);
}

